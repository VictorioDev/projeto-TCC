    private void validaBotoes() {
        if (tabelaNivel.getSelectedRow() == -1) {
            btAlterar.setEnabled(false);
            btExcluir.setEnabled(false);
        } else if (tabelaNivel.getSelectedRow() != -1) {
            btAlterar.setEnabled(true);
            btExcluir.setEnabled(true);
        }

    }

    private void AtualizaTabNivel() {
        try {
            listaNivel = NivelDao.RetornaNiveis(retornaObjeto());
            DefaultTableModel modeloniv = (DefaultTableModel) tabelaNivel.getModel();
            modeloniv.setNumRows(0);
            for (NivelBean niv : listaNivel) {
                modeloniv.addRow(new Object[]{
                    niv.getDescricao()
                });
            }

            if (listaNivel.size() == 0) {
                lbMensg.setText("Não há registro de nível");
            } else if (listaNivel.size() == 1) {
                lbMensg.setText("Há apenas um registro de nível");
            } else {
                lbMensg.setText("Há " + listaNivel.size() + " registros de níveis");
            }
            lbMensg.setForeground(Color.black);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

