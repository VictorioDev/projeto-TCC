    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        // TODO add your handling code here:
        if (tabelaNivel.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Nenhum nível selecionado para a alteração!", "ERRO!", JOptionPane.ERROR_MESSAGE);
            txNivel.requestFocus();
        } else {
            NivelBean nivel = listaNivel.get(tabelaNivel.getSelectedRow());
            new TelaNovoNivel(nivel).setVisible(true);
        }
    }//GEN-LAST:event_btAlterarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        // TODO add your handling code here:
        if (tabelaNivel.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Nenhum nível selecionado para a exclusão!", "ERRO!", JOptionPane.ERROR_MESSAGE);
            txNivel.requestFocus();
        } else {
            NivelBean nivel = listaNivel.get(tabelaNivel.getSelectedRow());
            try {
                int opc = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir a categoria?");
                if (opc == JOptionPane.YES_OPTION) {
                    NivelDao.DeletarNivel(nivel);
                    AtualizaTabNivel();
                    validaBotoes();
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btExcluirActionPerformed

    private void btImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btImprimirActionPerformed
        // TODO add your handling code here:
        try {
            if (txNivel.getText().trim().equalsIgnoreCase("")) {
                Relatorio.gerarRelatorio("relatorios\\ListagemDeNiveis.jasper", NivelDao.retornaRsSemObjeto());
            }else{
                Relatorio.gerarRelatorio("relatorios\\ListagemDeNiveis.jasper", NivelDao.retornaRsComObjeto(retornaObjeto()));
            }
        } catch (SQLException e) {
            e.printStackTrace();
        } catch (JRException ex) {
            ex.printStackTrace();
            Logger.getLogger(TelaPesquisarNivel.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btImprimirActionPerformed

