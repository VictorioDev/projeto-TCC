    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        // TODO add your handling code here:
        if (tabelaCategoria.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Nenhuma categoria selecionada para a alteração!", "ERRO!", JOptionPane.ERROR_MESSAGE);
            txCategoria.requestFocus();
        } else {
            CategoriaBean categoria = listaCategoria.get(tabelaCategoria.getSelectedRow());
            new TelaNovaCategoria(null, true, categoria).setVisible(true);
        }
    }//GEN-LAST:event_btAlterarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        // TODO add your handling code here:
        if (tabelaCategoria.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Nenhuma categoria selecionada para a exclusão!", "ERRO!", JOptionPane.ERROR_MESSAGE);
            txCategoria.requestFocus();
        } else {
            CategoriaBean categoria = listaCategoria.get(tabelaCategoria.getSelectedRow());
            try {
                int opc = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir a categoria?");
                if (opc == JOptionPane.YES_OPTION) {
                    CategoriaDao.excluir(categoria);
                    AtualizaTabCategoria();
                    validaBotoes();
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btExcluirActionPerformed

    private void btPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarActionPerformed
        // TODO add your handling code here:
        btAlterar.setEnabled(false);
        btExcluir.setEnabled(false);
        if (txCategoria.getText().length() > 1) {
            AtualizaTabCategoria();
        } else {
            lbCategoria.setForeground(Color.red);
            lbMensg.setText("A pesquisa deve ter ao menos 2 carcateres");
            lbMensg.setForeground(Color.red);
            DefaultTableModel modelo = (DefaultTableModel) tabelaCategoria.getModel();
            modelo.setNumRows(0);
        }
    }//GEN-LAST:event_btPesquisarActionPerformed

