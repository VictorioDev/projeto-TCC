    private void validaBotoes() {
        if (tabelaCategoria.getSelectedRow() == -1) {
            btAlterar.setEnabled(false);
            btExcluir.setEnabled(false);
        } else if (tabelaCategoria.getSelectedRow() != -1) {
            btAlterar.setEnabled(true);
            btExcluir.setEnabled(true);
        }

    }

    private void AtualizaTabCategoria() {
        try {
            listaCategoria = CategoriaDao.retornaCategoria(retornaObjeto());
            DefaultTableModel modelocateg = (DefaultTableModel) tabelaCategoria.getModel();
            modelocateg.setNumRows(0);
            for (CategoriaBean cat : listaCategoria) {
                modelocateg.addRow(new Object[]{
                    cat.getDescricao()
                });
            }

            if (listaCategoria.size() == 0) {
                lbMensg.setText("Não há registro de categoria");
            } else if (listaCategoria.size() == 1) {
                lbMensg.setText("Há apenas um registro de categoria");
            } else {
                lbMensg.setText("Há " + listaCategoria.size() + " registros de categoria");
            }
            lbMensg.setForeground(Color.black);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

